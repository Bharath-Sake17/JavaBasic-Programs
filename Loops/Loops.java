package com.scan;

public class Loops {
	public static void main(String[] args) {
		
		//for loop
		for(int i=1; i<= 5 ; i++)
		{
			System.out.println(i + "  Hello");
		}
		
		//while loop
		int a=1;
		while(a<=5)
		{
			System.out.println(a + " World");
			a++;
		}
		//do while
		
		int b=1;
		 do
		 {
			 System.out.println(b + " Java");
			 b++;
			 
		 }while(b<6);
	}

}
